
/**
 * Build & Environment Initialization Script
 * 
 * 该脚本在构建或启动流程中运行，主要承担以下职责：
 * 1. 构建信息记录：生成 `js/compiled/build-info.js`，包含版本号、构建时间戳，
 *    并在浏览器控制台打印带样式的版本信息，方便线上调试。
 * 2. 缓存消除 (Cache Busting)：自动更新 `index.html` 中引用资源的 URL 参数（?v=...），
 *    确保用户浏览器能加载到最新的代码，避免因缓存导致的问题。
 * 3. 配置自动同步：将 `src/config.js` 中的 API 配置同步到 `baseapi/constants.js`，
 *    确保源代码与旧版架构之间的配置一致性。
 */
const fs = require('fs');
const path = require('path');

const buildInfo = {
    version: process.env.npm_package_version || '1.0.0',
    timestamp: new Date().toISOString(),
    buildTime: new Date().toLocaleString()
};

const content = `/**
 * Build Information
 * Auto-generated by scripts/generate-build-info.js
 */
window.MagnesBuildInfo = ${JSON.stringify(buildInfo, null, 4)};
console.log('%c MAGNES STUDIO V${buildInfo.version} %c Built at: ${buildInfo.buildTime} ', 'background: #000; color: #fff; font-weight: bold; padding: 4px 8px;', 'background: #f0f0f0; color: #000; padding: 4px 8px;');
`;

const outputPath = path.join(__dirname, '../frontend/js/compiled/build-info.js');

// Ensure directory exists
const dir = path.dirname(outputPath);
if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
}

fs.writeFileSync(outputPath, content);
console.log('✅ Build info generated at:', outputPath);

// 自动化 index.html 版本号管理 (Cache Busting)
const indexPath = path.join(__dirname, '../frontend/index.html');
if (fs.existsSync(indexPath)) {
    let indexContent = fs.readFileSync(indexPath, 'utf8');

    // 生成短时间戳作为版本号，例如: 01231615
    const shortTS = new Date().toISOString()
        .replace(/[-T:]/g, '')
        .substring(4, 12);

    const newVersionTag = `?v=${buildInfo.version}_${shortTS}`;

    // 正则匹配: ?v= 或 ?v=数字.数字.数字 或 ?v=旧的时间戳
    const versionRegex = /\?v=[\d\._]+/g;

    if (versionRegex.test(indexContent)) {
        indexContent = indexContent.replace(versionRegex, newVersionTag);
        fs.writeFileSync(indexPath, indexContent);
        console.log(`🚀 Index.html resources updated to version: ${newVersionTag}`);
    }
}

// 自动同步配置到 frontend/core/constants.js
const sourceConfigPath = path.join(__dirname, '../frontend/src/config.js');
const baseConstantsPath = path.join(__dirname, '../frontend/core/constants.js');

if (fs.existsSync(sourceConfigPath) && fs.existsSync(baseConstantsPath)) {
    const srcContent = fs.readFileSync(sourceConfigPath, 'utf8');
    let baseContent = fs.readFileSync(baseConstantsPath, 'utf8');

    // 提取 src/config.js 中的 DEFAULT_API_CONFIGS 数组内容
    // 匹配 const DEFAULT_API_CONFIGS = [ ... ]; 之间的内容
    const configsMatch = srcContent.match(/const DEFAULT_API_CONFIGS = \[([\s\S]*?)\];/);

    if (configsMatch && configsMatch[1]) {
        const configsBody = configsMatch[1];

        // 替换 baseapi/constants.js 中的对应数组内容
        // 匹配 const DEFAULT_API_CONFIGS = [ ... ]; 并替换
        const baseRegex = /(const DEFAULT_API_CONFIGS = \[)([\s\S]*?)(\];)/;

        if (baseRegex.test(baseContent)) {
            baseContent = baseContent.replace(baseRegex, `$1${configsBody}$3`);
            fs.writeFileSync(baseConstantsPath, baseContent);
            console.log('✅ BaseAPI Constants synced with source config.');
        }
    }
}
